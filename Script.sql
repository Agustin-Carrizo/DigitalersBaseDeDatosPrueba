	Ciudad_Banco varchar(100),
	Nombre_Unico varchar(100),
	Id_cliente varchar(100),
	Direccion varchar(100),
	Ciudad_Cliente varchar(100)
);

create table if not exists Banco_Catamarca(
	Ciudad_Banco varchar(100),
	Nombre_Unico varchar(100),
	Id_cliente varchar(100),
	Direccion varchar(100),
	Ciudad_Cliente varchar(100)
);

create table if not exists Banco_Chaco(
	Ciudad_Banco varchar(100),
	Nombre_Unico varchar(100),
	Id_cliente varchar(100),
	Direccion varchar(100),
	Ciudad_Cliente varchar(100)
